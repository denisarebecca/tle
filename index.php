What is Covid 19?
<p align = "left"><Font size = "3"><Font color = "black"> <Font face = "Castellar">
Coronavirus disease (COVID-19),<br>
<br>is an infectious disease caused by a newly discovered coronavirus.
<br>Most people who fall sick with COVID-19 will experience mild to moderate symptoms and  
<br>recover without special treatment. In early 2020, following an outbreak in China in 
<br>December 2019, the World Health Organization described SARS-CoV-2 as a new form of coronavirus.
<br>
<br>The epidemic spread rapidly across the world. COVID-19 is a
SARS-CoV-2disease that can cause what doctors call respiratory tract infection.
<br>
<br>It can affect your upper respiratory tract (sinuses, nose, and throat) or lower respiratory tract (windpipe and lungs). 
It spreads in the same way as other coronaviruses do, mainly through person-to-person contact. 
Infections range from mild to fatal.
